const questions = [...rawQuestions].sort(() => Math.random() - 0.5);

let currentIndex = 0;

const questionTextEl = document.getElementById('questionText');
const userAnswerEl = document.getElementById('userAnswer');
const feedbackEl = document.getElementById('feedback');
const speakBtn = document.getElementById('speakBtn');
const skipBtn = document.getElementById('skipBtn');
const checkBtn = document.getElementById('checkBtn');
const nextBtn = document.getElementById('nextBtn');

function showQuestion() {
  if (currentIndex >= questions.length) {
    alert("Тест завершён!");
    location.reload();
    return;
  }
  const q = questions[currentIndex];
  questionTextEl.textContent = q.q;
  userAnswerEl.value = '';
  feedbackEl.style.display = 'none';
  feedbackEl.className = 'feedback';
  nextBtn.style.display = 'none';
  checkBtn.disabled = false;
  checkBtn.textContent = 'Проверить';
}

function speakQuestion() {
  const text = questionTextEl.textContent;
  if (!text) return;
  const utterance = new SpeechSynthesisUtterance(text);
  utterance.lang = 'ru-RU';
  utterance.rate = 0.95;
  utterance.pitch = 1;
  window.speechSynthesis.speak(utterance);
}

function checkAnswer() {
  const userInput = userAnswerEl.value.trim().toLowerCase();
  const correctAnswer = questions[currentIndex].a.toLowerCase();

  feedbackEl.style.display = 'block';
  checkBtn.disabled = true;

  if (userInput === correctAnswer) {
    feedbackEl.textContent = "✅ Верно! Молодец!";
    feedbackEl.classList.add('success');
  } else {
    feedbackEl.textContent = `❌ Неверно. Правильный ответ: ${questions[currentIndex].a}`;
    feedbackEl.classList.add('error');
  }

  nextBtn.style.display = 'inline-flex';
}

function goToNext() {
  currentIndex++;
  showQuestion();
}

function skipQuestion() {
  currentIndex++;
  showQuestion();
}

speakBtn.addEventListener('click', speakQuestion);
skipBtn.addEventListener('click', skipQuestion);
checkBtn.addEventListener('click', checkAnswer);
nextBtn.addEventListener('click', goToNext);

showQuestion();